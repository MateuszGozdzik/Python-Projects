morse = {
    # Alphabet
    'A': '.-',
    'B': '-...',
    'C': '-.-.',
    'D': '-..',
    'E': '.',
    'F': '..-.',
    'H': '....',
    'I': '..',
    'J': '.---',
    'K': '-.-',
    'L': '-..',
    'M': '--',
    'N': '-.',
    'O': '---',
    'P': '.--.',
    'Q': '--.-',
    'R': '.-.',
    'S': '...',
    'T': '-',
    'U': '..-',
    'V': '...-',
    'W': '.--',
    'X': '-..-',
    'Y': '-.--',
    'Z': '--..',

    # Numeric
    1: '.----',
    2: '..---',
    3: '...--',
    4: '....-',
    5: '.....',
    6: '-....',
    7: '--...',
    8: '---..',
    9: '----.',
    0: '-----',

    # Special Character
    '?': '..--..',
    '!': '-.-.--',
    '.': '.-.-.-',
    ',': '--..--',
    ';': '-.-.-.',
    ':': '---...',
    '+': '.-.-.',
    '-': '-....-',
    '/': '-..-.',
    '=': '-...-',

    # Space
    ' ': '/',
}

def text_to_morse( text: str ) -> str:
    tmp = ""
    for c in text.upper():
        tmp += morse[c] + " "
    return tmp[:-1]

print(text_to_morse("ABC"))